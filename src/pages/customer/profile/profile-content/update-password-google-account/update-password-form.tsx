"use client";

import { useState } from "react";

import { EyeIcon, EyeOffIcon } from "lucide-react";

import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
interface UpdatePasswordFormProps{
  handleUpdatePasswordGoogleAccount: (newPassword: string)=> Promise<any>
}
const UpdatePasswordForm = ({handleUpdatePasswordGoogleAccount}: UpdatePasswordFormProps) => {
  const [isPasswordVisible, setIsPasswordVisible] = useState(false);
  const [data, setData] = useState<string>('')

  return (
    <form className="space-y-4" onSubmit={(e) => e.preventDefault()}>
      {/* Password */}
      <div className="w-full space-y-1">
        <Label className="leading-5" htmlFor="password">
          Mật khẩu mới
        </Label>
        <div className="relative">
          <Input
            id="password"
            type={isPasswordVisible ? "text" : "password"}
            placeholder="••••••••••••••••"
            className="pr-9"
            onChange={(e)=>setData(e.target.value)}
          />
          <Button
            variant="ghost"
            size="icon"
            onClick={() => setIsPasswordVisible((prevState) => !prevState)}
            className="text-muted-foreground focus-visible:ring-ring/50 absolute inset-y-0 right-0 rounded-l-none hover:bg-transparent"
          >
            {isPasswordVisible ? <EyeOffIcon /> : <EyeIcon />}
            <span className="sr-only">
              {isPasswordVisible ? "Hide password" : "Show password"}
            </span>
          </Button>
        </div>
      </div>

      <Button onClick={()=> handleUpdatePasswordGoogleAccount(data)} className="w-full" type="submit">
        Cập nhật mật khẩu mới
      </Button>
    </form>
  );
};

export default UpdatePasswordForm;
